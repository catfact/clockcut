-- two values of N are included:

-- tapers.*_101: 
-- 101 entries, suitable for a percentage control 
-- each tick is 2/3 dB (in log section)
-- first 10% of range is linear from zero
-- last 90% is logarithmic in [-60db, 0db]

-- tapers.*_128:
-- 128 entries, suitable for MIDI control
-- first 2 ticks are linear from 0 to -63dB,
-- the rest are 1/2dB per tick
-- (this mirrors a common pattern in digital pots)

-- for each N there are two tables:
-- `amp_<N>`: amplitude values as numbers
-- `db_<N>`: decibel equivalents as strings

